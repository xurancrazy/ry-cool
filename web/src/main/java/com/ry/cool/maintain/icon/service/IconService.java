package com.ry.cool.maintain.icon.service;

import com.ry.cool.common.service.BaseService;
import com.ry.cool.maintain.icon.entity.Icon;
import com.ry.cool.maintain.icon.repository.IconRepository;
import org.springframework.stereotype.Service;

/**
 * <p>User: Zhang Kaitao
 * <p>Date: 13-2-4 下午3:01
 * <p>Version: 1.0
 */
@Service
public class IconService extends BaseService<Icon, Long> {

    private IconRepository getIconRepository() {
        return (IconRepository) baseRepository;
    }

    public Icon findByIdentity(String identity) {
        return getIconRepository().findByIdentity(identity);
    }
}
